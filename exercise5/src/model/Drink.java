package model;

class Drink {

    private String alcohol;
    private String taste;

    public String getAlcohol() {
        return this.alcohol;
    }

    public void setAlcohol(String alcohol) {
        this.alcohol = alcohol;
    }

    public String getTaste() {
        return this.taste;
    }

    public void setTaste(String taste) {
        this.taste = taste;
    }
    
}
